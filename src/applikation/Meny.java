    public String getInloggadAnvandareAid()
    {
        try{
        String sqlFraga = "SELECT aid from anstalld WHERE epost = '" + inloggadAnvandare + "'";
        String dbAid = idb.fetchSingle(sqlFraga);
        
        }
        catch(InfException ex){
        System.out.println(ex.getMessage());    
        }  
        return null;
        }
    
         
