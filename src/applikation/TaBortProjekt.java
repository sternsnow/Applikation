    private void btnTaBortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaBortActionPerformed
         try {
        String valdStrang = cbxProjekt.getSelectedItem().toString();
        this.valtProjekt = valdStrang;

        // Hämta pid för valt projekt
        String sqlFraga = "SELECT pid FROM projekt WHERE projektnamn = '" + valtProjekt + "'";
        String hamtatPid = idb.fetchSingle(sqlFraga);

        if (hamtatPid != null) {
            int svar = JOptionPane.showConfirmDialog(null,
                    "Är du säker på att du vill ta bort projektet \"" + valtProjekt + "\"?",
                    "Bekräfta borttagning",
                    JOptionPane.YES_NO_OPTION);

            if (svar == JOptionPane.YES_OPTION) {
                
                // Ta bort relationer till anställda i ans_proj
                String kontrollAnsProjFraga = "SELECT * FROM ans_proj WHERE pid = '" + hamtatPid + "'";
                HashMap<String, String> ansProjCheck = idb.fetchRow(kontrollAnsProjFraga);
                if (ansProjCheck != null) {
                    String deleteAnsProjFraga = "DELETE FROM ans_proj WHERE pid = '" + hamtatPid + "'";
                    idb.delete(deleteAnsProjFraga);
                }

                // Ta bort från projekt_partner om det finns
                String kontrollPartnerFraga = "SELECT * FROM projekt_partner WHERE pid = '" + hamtatPid + "'";
                HashMap<String, String> partnerCheck = idb.fetchRow(kontrollPartnerFraga);
                if (partnerCheck != null) {
                    String deletePartnerFraga = "DELETE FROM projekt_partner WHERE pid = '" + hamtatPid + "'";
                    idb.delete(deletePartnerFraga);
                }

                // Ta bort från proj_hallbarhet om det finns
                String kontrollHallbarhetFraga = "SELECT * FROM proj_hallbarhet WHERE pid = '" + hamtatPid + "'";
                HashMap<String, String> hallbarhetCheck = idb.fetchRow(kontrollHallbarhetFraga);
                if (hallbarhetCheck != null) {
                    String deleteHallbarhetFraga = "DELETE FROM proj_hallbarhet WHERE pid = '" + hamtatPid + "'";
                    idb.delete(deleteHallbarhetFraga);
                }

                // Ta bort projektet från projekt-tabellen
                String deleteProjektFraga = "DELETE FROM projekt WHERE pid = '" + hamtatPid + "'";
                idb.delete(deleteProjektFraga);

                JOptionPane.showMessageDialog(null, "Projektet har tagits bort.");
                fyllCombobox(); // uppdatera listan med projekt
            }
        } else {
            JOptionPane.showMessageDialog(null, "Kunde inte hitta projektets PID.");
        }

    } catch (InfException ex) {
        System.out.println("Fel vid borttagning: " + ex.getMessage());
        JOptionPane.showMessageDialog(null, "Något gick fel vid borttagningen.");
    }

    }//GEN-LAST:event_btnTaBortActionPerformed

