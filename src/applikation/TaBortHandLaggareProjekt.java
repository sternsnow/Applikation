    public void fyllCombobox()
    {
        try{
        cbxHandlaggare.removeAllItems();
        Projekt projekt = new Projekt(idb);
        ArrayList<String> handlaggare = projekt.hamtaAllaHandlaggare(pid);
        if(handlaggare == null)
        {
           cbxHandlaggare.addItem("Det finns inga handläggare att ta bort."); 
        }
        
        else{
        for(String namn: handlaggare)
        {
            cbxHandlaggare.addItem(namn);
        }
        } 
        }
        catch(Exception ex){
        System.out.println(ex.getMessage());    
        } 
        
    }
