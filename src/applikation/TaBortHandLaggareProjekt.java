    private void btnTaBortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaBortActionPerformed
        try{
            Projekt projekt = new Projekt(idb);
            String valdHandlaggare = cbxHandlaggare.getSelectedItem().toString();

            int svar = JOptionPane.showConfirmDialog(null,
                    "Är du säker på att du vill ta bort " + valdHandlaggare + " från projektet?",
                    "Bekräfta borttagning",
                    JOptionPane.YES_NO_OPTION);

            if (svar == JOptionPane.YES_OPTION) {
            
            projekt.TaBortHandlaggareIProjekt(pid, valdHandlaggare);
            JOptionPane.showMessageDialog(null, "Vald handläggare har tagits bort från projektet.");
            fyllCombobox();
            }
        }
        
        catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        
    }//GEN-LAST:event_btnTaBortActionPerformed

